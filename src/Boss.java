public class Boss {
    private int Healths;
    private int Damage;
    private String Defence;
    Boss(){

    }
    public int getHealths() {
        return Healths;
    }
    public void setHealths(int healths) {
        Healths = healths;
    }
    public int getDamage() {
        return Damage;
    }
    public void setDamage(int damage) {
        Damage = damage;
    }
    public String getDefence() {
        return Defence;
    }
    public void setDefence(String defence) {
        Defence = defence;
    }

}
